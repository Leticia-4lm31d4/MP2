package model;

public class Ponto {

    private int codigo;
    private String rua;
    private int numero_rua;

    public int getCodigo() {
        return this.codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getRua() {
        return this.rua;
    }

    public void setRua(String rua) {
        this.rua = rua;
    }

    public int getNumeroRua() {
        return this.numero_rua;
    }

    public void setNumeroRua(int numero_rua) {
        this.numero_rua = numero_rua;
    }
}
